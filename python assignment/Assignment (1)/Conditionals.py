# Conditional Statements

# i) if statement
num = 10
if num > 0:
    print(f"{num} is a positive number.")

# ii) if else statement
num = -5
if num > 0:
    print(f"{num} is a positive number.")
else:
    print(f"{num} is a negative number.")

